    private int retornaId(JList list){
        String valores = list.getModel().toString();
        String vetor[] = new String[list.getModel().getSize()];
        vetor = valores.split(",");
        String selecionado = vetor[list.getSelectionModel().getMaxSelectionIndex()];
        int id = 0;
        
        id = Integer.parseInt(selecionado.substring(1,selecionado.indexOf("-")).trim());
        
        return id;
    }
   
    private void loadListModel(JList list, ArrayList<Object>vObjects) {
        DefaultListModel listModel = (DefaultListModel) list.getModel();
        listModel.clear();
        for (int i = 0; i < vObjects.size(); i++) {
            listModel.addElement(vObjects.get(i));
        }
        /*for (Object o : vObjects) {
            
            listModel.addElement(o);
        }*/
    }
    
