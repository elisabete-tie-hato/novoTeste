    private void lstContaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lstContaMouseClicked
        String valores = this.lstConta.getModel().toString();
        String vetor[] = new String[this.lstConta.getModel().getSize()];
        vetor = valores.split(",");
        String selecionado = vetor[this.lstConta.getSelectionModel().getMaxSelectionIndex()];
        txtContaOp.setText(selecionado);
        
        vetor = selecionado.split(":");
        txtSaldo.setText(vetor[1].substring(0, vetor[1].indexOf("-")));
        
        if(vetor[1].contains("Limite")){
            txtLimiteOp.setEnabled(true);
            tipo = 1;
            txtLimiteOp.setText(vetor[2].substring(0, vetor[2].indexOf("-")));
        }else{
            tipo = 0;
            txtLimiteOp.setEnabled(false);
            txtLimiteOp.setText("");       
        }
        
        txtValor.setText("");
    }//GEN-LAST:event_lstContaMouseClicked

